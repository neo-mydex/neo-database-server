-- 猫角色映射表：存储 81 种交易性格组合的角色名称和描述
-- 维度：risk_level（风险偏好）/ speed_level（决策速度）/ info_level（信息辨别）/ patience_level（持仓耐心）
-- 等级映射：低(1-3)→1, 中(4-7)→2, 高(8-10)→3

CREATE TABLE IF NOT EXISTS ai_cat_map (
  id              SMALLINT    PRIMARY KEY,
  risk_level      SMALLINT    NOT NULL CHECK (risk_level     BETWEEN 1 AND 3),
  speed_level     SMALLINT    NOT NULL CHECK (speed_level    BETWEEN 1 AND 3),
  info_level      SMALLINT    NOT NULL CHECK (info_level     BETWEEN 1 AND 3),
  patience_level  SMALLINT    NOT NULL CHECK (patience_level BETWEEN 1 AND 3),
  cat_type        VARCHAR(32) NOT NULL,
  cat_desc        TEXT        NOT NULL
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_cat_map_levels
  ON ai_cat_map (risk_level, speed_level, info_level, patience_level);

